/**
 * GeoJSON map data for all 75 Diplomacy provinces.
 *
 * Coordinates are approximate real-world lat/lng covering Europe,
 * North Africa, and the Middle East. Designed for a geoMercator projection.
 *
 * Each Feature has properties: { id, name, type, supplyCenter }
 * matching the province codes in map-data.ts.
 */
import type { FeatureCollection, Feature, Polygon } from 'geojson';

export interface ProvinceGeoProperties {
  id: string;
  name: string;
  type: 'inland' | 'water' | 'coastal';
  supplyCenter: boolean;
}

function p(
  id: string,
  name: string,
  type: 'inland' | 'water' | 'coastal',
  supplyCenter: boolean,
  coords: number[][],
): Feature<Polygon, ProvinceGeoProperties> {
  return {
    type: 'Feature',
    properties: { id, name, type, supplyCenter },
    geometry: {
      type: 'Polygon',
      coordinates: [coords.concat([coords[0]!])], // close the ring
    },
  };
}

